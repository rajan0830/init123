package org.loop;

public class Switch {
	
	public static void main(String[] args) {
		String day = "Wednesday";
		
		switch (day) {
		
		case "Monday":
			System.out.println("Welcome to case: 1");
			break;
			
		case "Tuesday":
			System.out.println("Welcome to case: 2");
			break;
			
		case "Wednesday":
			System.out.println("Welcome to case: 3");
			break;
			
		default:
			System.out.println("Nothing");
			break;
		}
		
		
		
		
		
		
	}
}
