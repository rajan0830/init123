package org.loop;

public class TablesUsingLoop {
	
	
	
	public static void main(String[] args) {
		int j=13;
		System.out.println(j+" Tables");
		System.out.println("=============");

		for (int i=1; i<=12; i++) {
			
			
			      
				
				int a = i*j;
				
				

				System.out.println(j+" * "+i+" = "+a);
				
				
			}
			
		}
		
		
	}


